"""
找出100~999之间的所有水仙花数
水仙花数是各位立方和等于这个数本身的数
如: 153 = 1**3 + 5**3 + 3**3
"""

for x in range(100,1000,1):
    i = x //100;
    j = ( x - i * 100 ) // 10;
    k = ( x - i * 100 - j * 10 );
    if( x == i ** 3 + j ** 3 + k ** 3 ):
        print("[%d]=[%d]**3+[%d]**3+[%d]**3" % ( x, i, j, k ), end='\n' );
    
